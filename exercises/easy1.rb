class CircularQueue
  attr_reader :queue
  
  def initialize(buffer_size)
    @queue = Array.new(buffer_size, nil)
  end
  
  def dequeue
    queue << nil
    queue.shift
  end
  
  def enqueue(obj)
    if queue.any?(&:nil?)
      queue[queue.find_index(&:nil?)] = obj
    else
      queue.shift
      queue << obj
    end
  end
end

